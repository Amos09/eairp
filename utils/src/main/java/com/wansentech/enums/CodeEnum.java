package com.wansentech.enums;
public enum CodeEnum {

    // 基本状态码
    SUCCESS("200", "成功"),
    ERROR("500", "失败，服务器错误");

    /**
     * 响应状态码
     */
    private String code;

    /**
     * 响应提示
     */
    private String msg;

    CodeEnum(String code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
